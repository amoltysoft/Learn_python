#  data type 
# type function  return data type
#----------------
print (3, " " ,type(3)) # will return int 👉 integer
print (2, " " ,type(2)) # will return int 👉 integer
print (1, " ",type(1)) # will return int 👉 integer
print (1.5, " " ,type(1.5)) # will return float 👉 floating number
print (10.60," ",type(10.60)) # will return float 👉 floating number

print ("python ",type("python ")) # will return str 👉 string 

print ([1,2,3], " " ,type([1,2,3])) # will return  list 👉  lists
print ((1,2,3), " " ,type((1,2,3))) # will return tuple 👉  tuples
print ({ 1:"One" , 2:"Tow" , 3:"Three"} ,
" " , type ({1:"One",2:"Tow",3:"Three"})) #will return dict 👉  dictionary
print (2==3 ," " , type( 2==3)) # will return bool 👉  boolean 

